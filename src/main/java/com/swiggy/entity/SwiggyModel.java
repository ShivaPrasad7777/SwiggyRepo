package com.swiggy.entity;

public class SwiggyModel {
	private String name;
	private int mblnum;
	private String mail;
	private String pwd;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getMblnum() {
		return mblnum;
	}
	public void setMblnum(int mblnum) {
		this.mblnum = mblnum;
	}
	public String getMail() {
		return mail;
	}
	public void setMail(String mail) {
		this.mail = mail;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}

}
